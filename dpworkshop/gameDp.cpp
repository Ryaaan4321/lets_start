int n;
int dp[1001];
int dpCode(int x)
{
    if (x == 0)
    {
        return 0;
    }
    if (dp[x] != -1)
    {
        return dp[x];
    }
    int ans = 0;
    for (int m = 0; (1 << m) <= x; ++m)
    {
        if (dpCode(x - (1 << m)) == 0)
        {
            ans = 1;
            break;
        }
    }
    return dp[x] = ans;
}
